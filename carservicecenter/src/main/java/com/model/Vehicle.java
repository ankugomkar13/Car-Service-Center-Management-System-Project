package main.java.com.model;

public class Vehicle 
{
	private int vehicle_id;
	private String vehicle_owner_name ;
	private String vehicle_mobile ;
	private String vehicle_email ;
	private String vehicle_company;
	private String vehicle_model;
	private String vehicle_type;
	private String check_in_data;
	private String check_out_date;
	
	
	
	
	
	public String getCheck_in_data() {
		return check_in_data;
	}
	public void setCheck_in_data(String check_in_data) {
		this.check_in_data = check_in_data;
	}
	public String getCheck_out_date() {
		return check_out_date;
	}
	public void setCheck_out_date(String check_out_date) {
		this.check_out_date = check_out_date;
	}
	public String getVehicle_model() {
		return vehicle_model;
	}
	public void setVehicle_model(String vehicle_model) {
		this.vehicle_model = vehicle_model;
	}
	public int getVehicle_id() {
		return vehicle_id;
	}
	public void setVehicle_id(int vehicle_id) {
		this.vehicle_id = vehicle_id;
	}
	public String getVehicle_owner_name() {
		return vehicle_owner_name;
	}
	public void setVehicle_owner_name(String vehicle_owner_name) {
		this.vehicle_owner_name = vehicle_owner_name;
	}
	public String getVehicle_mobile() {
		return vehicle_mobile;
	}
	public void setVehicle_mobile(String vehicle_mobile) {
		this.vehicle_mobile = vehicle_mobile;
	}
	public String getVehicle_email() {
		return vehicle_email;
	}
	public void setVehicle_email(String vehicle_email) {
		this.vehicle_email = vehicle_email;
	}
	public String getVehicle_company() {
		return vehicle_company;
	}
	public void setVehicle_company(String vehicle_company) {
		this.vehicle_company = vehicle_company;
	}
	public String getVehicle_type() {
		return vehicle_type;
	}
	public void setVehicle_type(String vehicle_type) {
		this.vehicle_type = vehicle_type;
	}
	
	
}
